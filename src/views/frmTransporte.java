    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int fila = tblTransporte.getSelectedRow();
        if (fila != -1) {
            winTransporte.setTitle("Editar");
            winTransporte.setSize(418, 497);
            txtId.setEditable(false);
            winTransporte.setVisible(true);
            String idTransporte = tblTransporte.getValueAt(fila,0).toString();
            Transporte trasporte = transporte.buscarTrasporte(idTransporte);
            txtId.setText(trasporte.getIdTransporte());
            txtDestino.setText(trasporte.getDestino());
            txtKilom.setText(trasporte.getKilometros());
            txtCosto.setText(trasporte.getCosto());
            txtMatricula.setText(trasporte.getMatricula());
             Date date = Date.valueOf(trasporte.getFecha_trasporte());
            Calendar cal = Calendar.getInstance();
            cal.setTime(date);
            cbo_Fecha_Trans.setSelectedDate(cal);
            
            
        }else {
               JOptionPane.showMessageDialog(rootPane, "Elija el cliente a editar", "Editar", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
         int fila = tblTransporte.getSelectedRow();
        if (fila != -1) {
            String idTransporte = tblTransporte.getValueAt(fila, 0).toString();
            int resp = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que dessea eliminar a " + idTransporte, "Eliminar", JOptionPane.YES_NO_OPTION);

            if (resp == JOptionPane.YES_OPTION) {
                if (transporte.eliminarTransporte(idTransporte)) {
                    JOptionPane.showMessageDialog(rootPane, "Eliminado");
                    cargarTrasporte();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "problemas...");
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Seleccionar una fila a eliminar", "Eliminar", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

