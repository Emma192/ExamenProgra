    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        String msgError = ValidarDatos();
        if (msgError.equals("")) {
            Transporte trans= new Transporte();
            trans.setIdTransporte(txtId.getText());
            trans.setDestino(txtDestino.getText());
            trans.setKilometros(txtKilom.getText());
            trans.setCosto(txtCosto.getText());
            String Fechas_trasporte = new SimpleDateFormat("dd/MM/yyyy").format(cbo_Fecha_Trans.getSelectedDate().getTime());
            trans.setFecha_trasporte(Fechas_trasporte);
            trans.setMatricula(txtMatricula.getText());
            
            
            
            if (winTransporte.getTitle().equals("Guardar")) {
                if (transporte.buscarTrasporte(trans.getIdTransporte()) == null) {
                    if (transporte.insertarTransporte(trans)) {
                        cargarTrasporte();
                        JOptionPane.showMessageDialog(winTransporte,
                                "Trasporte guardado exitosamente", "Guardar",
                                JOptionPane.INFORMATION_MESSAGE);
                        winTransporte.dispose();
                    } else {
                        JOptionPane.showMessageDialog(winTransporte,
                                "Error al intentar guardar ", "Guardar",
                                JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(winTransporte, "Identificacion ya registrada",
                            "Guardar", JOptionPane.ERROR_MESSAGE);
                }

            } else {//Desea editar
                if (transporte.editarTransporte(trans)) {
                    cargarTrasporte();
                    JOptionPane.showMessageDialog(winTransporte,
                            "Cliente editado exitosamente", "Editar",
                            JOptionPane.INFORMATION_MESSAGE);
                    winTransporte.dispose();
                } else {
                    JOptionPane.showMessageDialog(winTransporte,
                            "Error al intentar editar", "Editar",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {//Si hay errores
            JOptionPane.showMessageDialog(winTransporte, msgError, "Guardar",
                    JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_btnAceptarActionPerformed

